class Add 
{
    public static void add(int n1,int n2)
    {
        int x=n1+n2;
        System.out.println(n1+"+"+n2+"="+x);
    }
    public static void add(int n1,int n2,int n3)
    {
        int x=n1+n2+n3;
        System.out.println(n1+"+"+n2+"+"+n3+"="+x);
    }
    public static void add(int n1,int n2,int n3,int n4,int n5)
    {
        int x=n1+n2+n3+n4+n5;
        System.out.println(n1+"+"+n2+"+"+n3+"+"+n4+"+"+n5+"="+x);
    }
    public static void add(int n1,int n2,int n3,int n4,int n5,int n6)
    {
        int x=n1+n2+n3+n4+n5+n6;
        System.out.println(n1+"+"+n2+"+"+n3+"+"+n4+"+"+n5+"+"+n6+"="+x);
    }
}
